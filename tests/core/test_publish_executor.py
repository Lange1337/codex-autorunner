def _parse_utc(value: str) -> datetime:
    return datetime.fromisoformat(value.replace("Z", "+00:00")).astimezone(timezone.utc)


class _QueuedClock:
    def __init__(self, *timestamps: str) -> None:
        self._timestamps = [_parse_utc(value) for value in timestamps]
        self._index = 0

    def __call__(self) -> datetime:
        if not self._timestamps:
            raise AssertionError("clock requires at least one timestamp")
        index = min(self._index, len(self._timestamps) - 1)
        self._index += 1
        return self._timestamps[index]


def _attempt_rows(hub_root: Path, operation_id: str) -> list[dict[str, object]]:
    with open_orchestration_sqlite(hub_root) as conn:
        rows = conn.execute(
            """
            SELECT attempt_number, state, response_json, error_text, claimed_at, started_at, finished_at
              FROM orch_publish_attempts
             WHERE operation_id = ?
             ORDER BY attempt_number ASC
            """,
            (operation_id,),
        ).fetchall()
    return [dict(row) for row in rows]


def _publish_hub(tmp_path: Path) -> tuple[Path, Path, str]:
    hub_root = tmp_path / "hub"
    ca_dir = hub_root / ".codex-autorunner"
    ca_dir.mkdir(parents=True, exist_ok=True)
    (ca_dir / ".gitignore").write_text("*", encoding="utf-8")
    cfg = json.loads(json.dumps(DEFAULT_HUB_CONFIG))
    cfg["discord_bot"]["enabled"] = True
    cfg["telegram_bot"]["enabled"] = True
    write_test_config(hub_root / CONFIG_FILENAME, cfg)
    workspace_root = (hub_root / "worktrees" / "repo-publish").resolve()
    workspace_root.mkdir(parents=True, exist_ok=True)
    (ca_dir / "manifest.yml").write_text(
        "\n".join(
            [
                "# GENERATED by CAR - DO NOT EDIT",
                "version: 3",
                "repos:",
                "  - id: repo-publish",
                f"    path: {workspace_root.relative_to(hub_root).as_posix()}",
                "    enabled: true",
                "    auto_run: false",
                "    kind: base",
                "",
            ]
        ),
        encoding="utf-8",
    )
    (ca_dir / "hub_state.json").write_text(
        '{\n  "last_scan_at": null,\n  "repos": [],\n  "pinned_parent_repo_ids": []\n}\n',
        encoding="utf-8",
    )
    return hub_root, workspace_root, "repo-publish"


