def test_enqueue_managed_turn_executor_reuses_existing_execution_for_same_operation(
    tmp_path: Path,
) -> None:
    hub_root, workspace_root, repo_id = _publish_hub(tmp_path)
    thread_store = PmaThreadStore(hub_root)
