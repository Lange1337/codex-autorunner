def _hub(tmp_path: Path) -> Path:
    hub_root = tmp_path / "hub"
    seed_hub_files(hub_root, force=True)
    cfg = json.loads(json.dumps(DEFAULT_HUB_CONFIG))
    cfg["discord_bot"]["enabled"] = True
    cfg["telegram_bot"]["enabled"] = True
    write_test_config(hub_root / CONFIG_FILENAME, cfg)
    return hub_root


def _write_manifest(hub_root: Path, repo_id: str, workspace: Path) -> None:
    manifest_path = hub_root / ".codex-autorunner" / "manifest.yml"
    rel_path = workspace.relative_to(hub_root).as_posix()
    manifest_path.write_text(
        "\n".join(
            [
                "# GENERATED by CAR - DO NOT EDIT",
                "version: 3",
                "repos:",
                f"  - id: {repo_id}",
                f"    path: {rel_path}",
                "    enabled: true",
                "    auto_run: false",
                "    kind: base",
                "",
            ]
        ),
        encoding="utf-8",
    )


def _create_bound_thread(
    hub_root: Path, workspace: Path, *, surface_kind: str, surface_key: str
) -> str:
    thread = PmaThreadStore(hub_root).create_thread("codex", workspace)
    thread_id = str(thread["managed_thread_id"])
    OrchestrationBindingStore(hub_root).upsert_binding(
        surface_kind=surface_kind,
        surface_key=surface_key,
        thread_target_id=thread_id,
    )
    return thread_id


def _set_discord_binding_updated_at(
    state_path: Path, channel_id: str, updated_at: str
) -> None:
    conn = sqlite3.connect(state_path)
    try:
        conn.execute(
            "UPDATE channel_bindings SET updated_at = ? WHERE channel_id = ?",
            (updated_at, channel_id),
        )
        conn.commit()
    finally:
        conn.close()


def _set_telegram_topic_updated_at(state_path: Path, key: str, updated_at: str) -> None:
    conn = sqlite3.connect(state_path)
    try:
        conn.execute(
            "UPDATE telegram_topics SET updated_at = ?, last_active_at = ? WHERE topic_key = ?",
            (updated_at, updated_at, key),
        )
        conn.commit()
    finally:
        conn.close()


@pytest.mark.anyio
async def test_notify_primary_pma_chat_prefers_freshest_matching_discord_binding(
    tmp_path: Path,
) -> None:
    hub_root = _hub(tmp_path)
    workspace = (hub_root / "worktrees" / "repo-a").resolve()
    workspace.mkdir(parents=True, exist_ok=True)
    _write_manifest(hub_root, "repo-a", workspace)

    discord_store = DiscordStateStore(
        hub_root / ".codex-autorunner" / "discord_state.sqlite3"
    )
    telegram_store = TelegramStateStore(
        hub_root / ".codex-autorunner" / "telegram_state.sqlite3"
    )
    try:
        await discord_store.upsert_binding(
            channel_id="older-discord",
            guild_id="guild-1",
            workspace_path=str(workspace),
            repo_id="repo-a",
        )
        await discord_store.update_pma_state(
            channel_id="older-discord",
            pma_enabled=True,
            pma_prev_workspace_path=str(workspace),
            pma_prev_repo_id="repo-a",
        )
        _set_discord_binding_updated_at(
            hub_root / ".codex-autorunner" / "discord_state.sqlite3",
            "older-discord",
            "2026-03-18T08:22:01Z",
        )

        await discord_store.upsert_binding(
            channel_id="newer-discord",
            guild_id="guild-1",
            workspace_path=str(workspace),
            repo_id="repo-a",
        )
        await discord_store.update_pma_state(
            channel_id="newer-discord",
            pma_enabled=True,
            pma_prev_workspace_path=str(workspace),
            pma_prev_repo_id="repo-a",
        )
        _set_discord_binding_updated_at(
            hub_root / ".codex-autorunner" / "discord_state.sqlite3",
            "newer-discord",
            "2026-03-18T08:22:09Z",
        )

        await telegram_store.bind_topic(
            topic_key(1001, 2002),
            str(workspace),
            repo_id="repo-a",
        )
        telegram_state = await telegram_store.load()
        telegram_topic = telegram_state.topics[topic_key(1001, 2002)]
        telegram_topic.pma_enabled = True
        telegram_topic.pma_prev_repo_id = "repo-a"
        await telegram_store.save(telegram_state)
        _set_telegram_topic_updated_at(
            hub_root / ".codex-autorunner" / "telegram_state.sqlite3",
            topic_key(1001, 2002),
            "2026-03-18T08:22:05Z",
        )

        outcome = await notify_primary_pma_chat_for_repo(
            hub_root=hub_root,
            repo_id="repo-a",
            message="Escalation message",
            correlation_id="corr-1",
        )

        assert outcome["targets"] == 1
        assert outcome["published"] == 1

        await discord_store.close()
        await telegram_store.close()
        discord_store = DiscordStateStore(
            hub_root / ".codex-autorunner" / "discord_state.sqlite3"
        )
        telegram_store = TelegramStateStore(
            hub_root / ".codex-autorunner" / "telegram_state.sqlite3"
        )
        discord_outbox = await discord_store.list_outbox()
        telegram_outbox = await telegram_store.list_outbox()
        assert any(
            record.channel_id == "newer-discord"
            and record.payload_json.get("content") == "Escalation message"
            for record in discord_outbox
        )
        assert not telegram_outbox
    finally:
        await discord_store.close()
        await telegram_store.close()


@pytest.mark.anyio
async def test_deliver_pma_notification_persists_replyable_context_for_bound_delivery(
    tmp_path: Path,
) -> None:
    hub_root = _hub(tmp_path)
    workspace = (hub_root / "worktrees" / "repo-f").resolve()
    workspace.mkdir(parents=True, exist_ok=True)
    _write_manifest(hub_root, "repo-f", workspace)

    discord_store = DiscordStateStore(
        hub_root / ".codex-autorunner" / "discord_state.sqlite3"
    )
    try:
        await discord_store.upsert_binding(
            channel_id="repo-f-discord",
            guild_id="guild-1",
            workspace_path=str(workspace),
            repo_id="repo-f",
        )
        _set_discord_binding_updated_at(
            hub_root / ".codex-autorunner" / "discord_state.sqlite3",
            "repo-f-discord",
            "2026-03-18T11:00:01Z",
        )

        outcome = await deliver_pma_notification(
            hub_root=hub_root,
            workspace_root=workspace,
            repo_id="repo-f",
            message="Dispatch needs review",
            correlation_id="corr-6",
            delivery="auto",
            source_kind="dispatch_paused",
            run_id="run-123",
            managed_thread_id="thread-123",
            context_payload={"wake_up": {"kind": "dispatch_paused"}},
        )

        assert outcome == {"route": "bound", "targets": 1, "published": 1}

        outbox = await discord_store.list_outbox()
        assert len(outbox) == 1
        record = outbox[0]
        assert record.channel_id == "repo-f-discord"
        assert record.payload_json.get("content") == "Dispatch needs review"

        notification_store = PmaNotificationStore(hub_root)
        delivered = notification_store.mark_delivered(
            delivery_record_id=record.record_id,
            delivered_message_id="discord-msg-123",
        )
        assert delivered is not None
        assert delivered.delivery_mode == "bound"
        assert delivered.repo_id == "repo-f"
        assert delivered.workspace_root == str(workspace)
        assert delivered.run_id == "run-123"
        assert delivered.managed_thread_id == "thread-123"
        assert delivered.context == {
            "message": "Dispatch needs review",
            "correlation_id": "corr-6",
            "source_kind": "dispatch_paused",
            "wake_up": {"kind": "dispatch_paused"},
        }

        reply_target = notification_store.get_reply_target(
            surface_kind="discord",
            surface_key="repo-f-discord",
            delivered_message_id="discord-msg-123",
        )
        assert reply_target == delivered
        context_block = build_notification_context_block(reply_target)
        assert "<notification_context>" in context_block
        assert '"notification_id"' in context_block
        assert '"dispatch_paused"' in context_block
    finally:
        await discord_store.close()


